    public boolean verification()
    {
        String name = jTextFieldName.getText();
        String lName = jTextFieldLName.getText();
        String username = jTextFieldUsername.getText();
        String pass = String.valueOf(jPasswordField.getPassword());
        String passC = String.valueOf(jPasswordFieldConfirm.getPassword());
        String email = jTextFieldEmail.getText();
        
        //check empty fields
        if(name.trim().equals("")||lName.trim().equals("")||username.trim().equals("")||pass.trim().equals("")||
           passC.trim().equals("")||email.trim().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Jedno lub więcej pól jest pusta. Uzupelnij je.","Empty fields",2);
            return false;
        }
        
        //check correct password
        else if(!pass.equals(passC))
        {
            JOptionPane.showMessageDialog(null, "Hasła nie sa takie same","Confirm Password",2);
            return false;
        }
        else
        {
            return true;
        }
    }
    
    public boolean checkUsername(String username)
    {
        PreparedStatement st;
        ResultSet rs;
        boolean usernameExist = false;
        
        String query = "SELECT * FROM `users` WHERE `username` = ?";
        
        try {
            st = ConnectionClass.getConnection().prepareStatement(query);
            st.setString(1, username);
            rs = st.executeQuery();
            
            if(rs.next())
            {
                usernameExist = true;
                JOptionPane.showMessageDialog(null, "Ten login jest zajety. Podaj inny login","Ussername failed",2);
            
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Register_Pane.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return usernameExist;
    }
    
    
    
    
