    private void jButtonRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegisterActionPerformed
        String name = jTextFieldName.getText();
        String lName = jTextFieldLName.getText();
        String username = jTextFieldUsername.getText();
        String pass = String.valueOf(jPasswordField.getPassword());
        String passC = String.valueOf(jPasswordFieldConfirm.getPassword());
        String email = jTextFieldEmail.getText();
        
        if(verification())
        {
            if(!checkUsername(username))
            {
                PreparedStatement ps;
                ResultSet rs;
                
                String registerQuery = "INSERT INTO `users`(`permissions`,`name`, `lastname`, `username`, `password`, `email`) VALUES (?,?,?,?,?,?)";
                
                try {
                    ps = ConnectionClass.getConnection().prepareStatement(registerQuery);
                    ps.setString(1, "user");
                    ps.setString(2, name);
                    ps.setString(3, lName);
                    ps.setString(4, username);
                    ps.setString(5, pass);
                    ps.setString(6, email);
                    
                    if(ps.executeUpdate() != 0)
                    {
                        JOptionPane.showMessageDialog(null, "Konto zostalo utworzone");
                        
                        Login_Pane lp = new Login_Pane();
                        lp.setVisible(true);
                        lp.pack();
                        this.dispose();
                    }else
                    {
                        JOptionPane.showMessageDialog(null, "Sprawdz czy dane sa poprawne");

                    }
                    
                } catch (SQLException ex) {
                    Logger.getLogger(Register_Pane.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            }
        }
        
    }//GEN-LAST:event_jButtonRegisterActionPerformed

