    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = jTextFieldID.getText();
        if(choose==1){
            PreparedStatement ps;
            ResultSet rs;

            //create query 
            String joinEventQuery = "UPDATE `joinevent` SET `wasAccepted`= ? WHERE `id` = ?";
            try {
                ps = ConnectionClass.getConnection().prepareStatement(joinEventQuery);
                ps.setString(1, decision);
                ps.setString(2, id);
                
                if(ps.executeUpdate() != 0)
                {
                    showEvent();
                }else
                {
                    JOptionPane.showMessageDialog(null, "Sprawdz czy dane sa poprawne");
                }
            } catch (SQLException ex) {
                Logger.getLogger(Login_Pane.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else
        {
            JOptionPane.showMessageDialog(null, "Nie podjales decyzji");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

