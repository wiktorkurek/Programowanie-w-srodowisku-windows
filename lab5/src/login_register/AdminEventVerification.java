    public ArrayList<EventCheck> eventAccept() throws SQLException{
        ArrayList<EventCheck> eventAccept = new ArrayList<>();
        try {
            Statement st = (Statement) ConnectionClass.getConnection().createStatement();
            //create query
            String eventCheckQuery = "SELECT * FROM joinevent";
            ResultSet rs = st.executeQuery(eventCheckQuery);
            EventCheck eventcheck;
            
            while(rs.next()){
                
                eventcheck = new EventCheck(rs.getInt("id"),rs.getString("eventName"), rs.getString("participation"), rs.getString("feed"), rs.getString("whoJoin"), rs.getString("wasAccepted"));
                eventAccept.add(eventcheck);
            }
            
            
        } catch (SQLException ex) {
                    Logger.getLogger(Register_Pane.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return eventAccept;        
    }
    
    public void showEvent(){
        ArrayList<EventCheck> list;
        try {
            list = eventAccept();
            DefaultTableModel modelAccept = (DefaultTableModel)jTableAccepted.getModel();
            DefaultTableModel modelNot = (DefaultTableModel)jTableNot.getModel();
            DefaultTableModel modelToDec = (DefaultTableModel)jTableToDec.getModel();
            Object[] row = new Object[3];
            Object[] row2 = new Object[5];
            modelAccept.setRowCount(0);
            modelNot.setRowCount(0);
            modelToDec.setRowCount(0);
            
            for(int i=0;i<list.size();i++){
                row[0] = list.get(i).getid();
                row[1] = list.get(i).geteventName();
                row[2] = list.get(i).getwhoJoin();
                row2[0] = list.get(i).getid();
                row2[1] = list.get(i).geteventName();
                row2[2] = list.get(i).getwhoJoin();
                row2[3] = list.get(i).getparticipation();
                row2[4] = list.get(i).getfeed();
                switch (list.get(i).getwasAccepted()) {
                    case "Nie":
                        modelNot.addRow(row);
                        break;
                    case "Tak":
                        modelAccept.addRow(row);
                        break;
                    case "Brak":
                        modelToDec.addRow(row2);
                        break;
                    default:
                        break;
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdminEventChange.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
